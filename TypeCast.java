public class TypeCast{
    public static void main(String[] args){
        int a = 258;

        byte b = (byte)a;

        float c = 10.2f;

        int d = (int)c;

        char e = 'e';

        int f = (int)e;

        System.out.println(b);
        System.out.println(d);
        System.out.println(f);
    }
}